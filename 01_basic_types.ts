// Basic Types (or static types)

// static typing to Primitive Data Type variables
let id: number = 5;
let company: string = 'Traversy Media';
let isPublished: boolean = true;
let x: any = 'Hello';

let age: number;
age = 23;

// static typing to Object or Reference Data Type variables
let arr: number[] = [1, 2, 3, 4, 5]; // arr with static typing having all its elements with type number
let arr1: any[] = [1, 2, true, 'hello']; // arr1 with static typing having all its elements with any type
